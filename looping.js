// For Loop
var i;
for(i = 1; i <= 5; i++){
    console.log(i);
}

console.log('============================');

// For of Loop
var food;
food = ['Lalapan', 'Geprek', 'Nasgor', 'Pecel'];
for(ArrayItem of food){
    console.log(ArrayItem);
}

console.log('============================');

// While Loop
var awal;
awal = 1;
while(awal <= 5){
    console.log(awal);
    awal++;
}

console.log('============================');

// Do While
var number;
number = 1;

do{
    console.log(number);
    number++;
}while(number <= 10);